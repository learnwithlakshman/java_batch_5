package com.careerit.cj.day4;

public class SumAndAverage {

		public static void main(String[] args) {
				int a = 10;
				int b = 25;
				int c = 30;
				
				int sum = a + b + c;
				float avg = sum / 3.0f;
				System.out.println("Sum :"+sum);
				System.out.println("Average : "+avg);
				
		}
}
